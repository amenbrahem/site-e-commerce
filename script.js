 // Fonction pour afficher une liste des catégories et des liens vers les produits
 function showCategories() {
    var categoriesList = document.getElementById('prod-list');
    categoriesList.style.display = 'block';
}

// Fonction pour masquer la liste des catégories
function hideCategories() {
    var categoriesList = document.getElementById('prod-list');
    categoriesList.style.display = 'none';
}